	function startTime() {
	  const today = new Date();
	  let h = today.getHours();
	  let m = today.getMinutes();
	  let s = today.getSeconds();
	  
	  hours = h % 12;
	  hours = hours ? hours : 12;
	  
	  var ampm = h >= 12 ? 'PM' : 'AM';
	  
	  m = checkTime(m);
	  s = checkTime(s);
	  if(document.getElementById('txt')){
		  document.getElementById('txt').innerHTML =  hours + ":" + m + " " + ampm;
	  }
	  if(document.getElementById('txtmob')){
		  document.getElementById('txtmob').innerHTML =  hours + ":" + m + " " + ampm;
	  }
	  setTimeout(startTime, 1000);
	}
	
